#Determinar cual es el mayor o si son iguales
print("Ingrese dos variables")
X=int(input("Ingrese la primera variable X= "))
Y=int(input("Ingrese la segunda variable Y= "))
if X>Y:
    print("X es mayor que Y")
elif X<Y:
    print("Y es mayor que X")
else:
    print("Las variables X e Y son iguales")